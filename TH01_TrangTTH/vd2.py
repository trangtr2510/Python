# Get input from the user
a = int(input("Nhập vào số a: "))
b = int(input("Nhập vào số b: "))

# Calculate the results
tong = a + b
hieu = a - b
tich = a * b
thuong = a / b

# Print the results
print("Tổng a + b =", tong)
print("Hiệu a - b =", hieu)
print("Tích a * b =", tich)
print("Thương a / b =", thuong)